// Create variables of types Boolean, String, and Number. Initialize these variables with values. 
// Boolean variable
let booleanVariable: boolean = true;

// String variable
let stringVariable: string = "Hello World!";

// Number variable
let numberVariable: number = 1;

// Create 3 variables of type Any and set their values “Bob”, 5, and true. 
// Any variables
let any1: any = "Bob";
let any2: any = 5
let any3: any = true;

// Send all 6 variables values to the console. 
console.log("Boolean variable: " + booleanVariable);
console.log("String variable: " + stringVariable);
console.log("Number variable: " + numberVariable);
console.log("Any variables: " + any1 + " , " + any2 + " , " + any3);